package projetojedi.conquista01;

/* Criar um código que verifica expressões booleanas (verdadeiro ou falso)
 utilizando operadores de comparação e operadores lógicos.
Ex: boolean ativo = true; ativo == true && ativo != false
*/
public class Exercicio07 {
    public static void main(String[] args) {
        int k = 4;
        int q = 10;
        boolean m = k > q;
        boolean n = k == q;
        boolean o = k < q;
        boolean p = k != q;
        System.out.println("Valor de m é: " + m);
        System.out.println("Valor de n é: " + n);
        System.out.println("Valor de o é: " + o);
        System.out.println("Valor de p é: " + p);
    }
}
